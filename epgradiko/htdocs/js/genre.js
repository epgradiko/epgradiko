	function true_genre(genre_name)
	{
		for( genre=0; genre<16; genre++ ){
			if( genre_name == genre_tbl[genre] )
				break;
		}
		return genre;
	}

	function sub_genre_view( genre, sub_code )
	{
		if( sub_code == 16 )
			document.write( "すべて" );
		else
		if( sub_code == 15 )
			document.write( "その他" );
		else
			if( genre > 0 )
				document.write( sub_genre_tbl[genre-1][sub_code] );
	}
